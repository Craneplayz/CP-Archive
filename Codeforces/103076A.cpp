template <typename T>
T EulerPhi(T n){
  T ans=n;
  for(T i=2;i*i<=n;i++){
    if(!(n%i)){
      ans=ans/i*(i-1);
      while(!(n%i)) n/=i;
    }
  }
  if(n>1) ans=ans/n*(n-1);
  return ans;
}

