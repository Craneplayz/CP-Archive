void solve(){
    int n,cd=0,ck=0;
    str a;
    mpiii d;
    cin>>n>>a;
    for(int i=0;i<n;i++){
        if(a[i]=='D') cd++;
        else ck++;
        int k=gcd(cd,ck);
        int cdp=cd/k;
        int ckp=ck/k;
        pii b=make_pair(cdp,ckp);
        if(d[b]>=1) d[b]++;
        else d[b]=1;
        cout<<d[b]<<" ";
    }
    cout<<endl;
}

